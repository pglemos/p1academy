export function getNewsTimestamp(post: NewsPost) {
  if (post.date.includes("/")) {
    const [day, month, year] = post.date.split("/").map(Number);
    return new Date(year, month - 1, day).getTime();
  }

  return new Date(post.date).getTime();
}

